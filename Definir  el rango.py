# Definir el número y el rango
numero = 15  # Puedes cambiar este número
rango_minimo = 10
rango_maximo = 100

# Comprobar si el número está dentro del rango
if numero >= rango_minimo and numero <= rango_maximo:
    print("El número", numero, "está dentro del rango.")
else:
    print("El número", numero, "está fuera del rango.")
